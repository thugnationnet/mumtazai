const ART_COLS = 108;
const ART_ROWS = 59;

const AsciiIdleScreen: React.FC = () => {
  const containerRef = useRef<HTMLDivElement>(null);
  const [fontSize, setFontSize] = useState(8);

  useEffect(() => {
    const el = containerRef.current;
    if (!el) return;
    const calc = () => {
      const w = el.clientWidth;
      const h = el.clientHeight;
      const byWidth = w / (ART_COLS * 0.602);
      const byHeight = h / (ART_ROWS * 1.1);
      setFontSize(Math.floor(Math.min(byWidth, byHeight) * 100) / 100);
    };
    calc();
    const ro = new ResizeObserver(calc);
    ro.observe(el);
    return () => ro.disconnect();
  }, []);

  return (
    <div ref={containerRef} className="flex items-center justify-center h-full w-full bg-white dark:bg-[#0a0a0a] overflow-hidden">
      <style>{`
        @keyframes rainbow-ascii {
          0% { color: #06b6d4; }
          14% { color: #8b5cf6; }
          28% { color: #ec4899; }
          42% { color: #f97316; }
          57% { color: #eab308; }
          71% { color: #22c55e; }
          85% { color: #3b82f6; }
          100% { color: #06b6d4; }
        }
        .ascii-rainbow {
          animation: rainbow-ascii 8s ease-in-out infinite;
          opacity: 0.7;
        }
      `}</style>
      <pre
        className="ascii-rainbow select-none font-mono whitespace-pre"
        style={{ fontSize: `${fontSize}px`, lineHeight: 1.1 }}
      >{ASCII_ART}</pre>
    </div>
  );
};

const Preview: React.FC<PreviewProps> = ({ code }) => {
  const iframeRef = useRef<HTMLIFrameElement>(null);

  useEffect(() => {
    if (iframeRef.current && code) {
      const doc = iframeRef.current.contentWindow?.document;
      if (doc) {
        // Inject error bridge before writing code
        const errorBridge = `<script>
          window.onerror = function(msg, src, line, col, err) {
            window.parent.postMessage({ type: 'preview-error', error: { message: String(msg), source: src, line: line, col: col, stack: err && err.stack } }, '*');
          };
          window.addEventListener('unhandledrejection', function(e) {
            window.parent.postMessage({ type: 'preview-error', error: { message: 'Unhandled Promise: ' + (e.reason && e.reason.message || String(e.reason)) } }, '*');
          });
        <\\/script>`;
        doc.open();
        doc.write(errorBridge + code);
        doc.close();
      }
    }
  }, [code]);

  if (!code) {
    return <AsciiIdleScreen />;
  }

  return (
    <div className="w-full h-full bg-white dark:bg-[#0a0a0a] shadow-sm overflow-hidden flex flex-col">
      <div className="flex items-center gap-2 px-4 py-2 bg-white dark:bg-[#111] border-b border-violet-900/30 text-xs text-slate-500">
        <div className="flex gap-1.5">
          <div className="w-3 h-3 rounded-full bg-red-400"></div>
          <div className="w-3 h-3 rounded-full bg-yellow-400"></div>
          <div className="w-3 h-3 rounded-full bg-indigo-400"></div>
        </div>
        <div className="flex-1 text-center font-mono opacity-60 truncate text-violet-400/60">
          localhost:3000/generated-app
        </div>
      </div>
      <iframe
        ref={iframeRef}
        title="App Preview"
        className="w-full h-full border-none"
        sandbox="allow-scripts allow-forms allow-popups allow-modals allow-downloads"
      />
    </div>
  );
};

export default Preview;
